#include <stdio.h>

int main() {

  // Augmented assignment operators = used to replace a statment where an operator takes a variable as one of its arguments and then assigns the result back to the same variable
  // Shortcut

  int x = 10;

  //x = x + 2;
  x += 2;

  // x = x - 2
  x -= 2;

  // x = x * 2
  x *= 2;

  // x = x / 2
  x /= 2;

  // x = x % 2
  x %= 2;

  return 0;
}