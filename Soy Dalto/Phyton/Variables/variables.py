#Definiendo variables

a = 5
b = 8
c = a + b

print (c)

nombre = "Shakir Chaya"

print (nombre)

numero = 15
numero += 5
numero -= 2

print (numero)

#Concatenar con +-strings

nombre = "Shakir"
bienvenida = "Hola " + nombre + " ¿Como estas?"
print (bienvenida)

#Concatenar con f-strings

nombre = "Shakir"
bienvenida = f"Hola {nombre} ¿Como estas?"
print (bienvenida)

#Operadores de pertenencia (in / not in)

print ("Shakir" in bienvenida) #True
print ("Shakir" not in bienvenida) #False