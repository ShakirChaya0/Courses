#Operadores Lógicos. and | or | not

print(3 > 4 and "Hola" > "Phyton")
print(3 < 4 and "Hola" < "Phyton")
print(3 > 4 or "Hola" > "Phyton")
print(3 < 4 or "Hola" < "Phyton")

print(not(3 > 4))