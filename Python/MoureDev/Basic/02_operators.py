#Operadores Aritméticos

print(10 + 4) #Suma
print(10 - 4) #Resta
print(10 * 4) #Multiplicación
print(10 / 4) #División
print(10 % 4) #Resto de una división
print(10 // 4) #División que devulve un numero entero, redondea hacia abajo
print(10 ** 4) #Potenciación

#Oeradores Aritméticos con Strings

print("Hola" + "Shakir") #Suma de string
print("Hola" + str(5)) #Suma de string
print("Hola" * 5) #Multiplicación de un string



#Operadores Comparativos

print(3 > 4) #Mayor que
print(3 < 4) #Menor que
print(3 >= 4) #Mayor o igual que
print(3 <= 4) #Menor o igual que
print(3 == 4) #Igual que
print(3 != 4) #Distinto que

#Operadores comparativos con strings. 

